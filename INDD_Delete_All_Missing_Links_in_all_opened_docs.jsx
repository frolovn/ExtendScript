var lengthN =app.documents.length;
 
for (var counter = 0; counter < lengthN; counter++)
{
 
 
    var doc = app.documents[counter];
    var links = doc.links;
 
    for (var i = 0; i<links.length; i++) {
        if (links[i].status == LinkStatus.LINK_MISSING) {
            links[i].parent.remove();
        }
  }
} 

